let n1 = Number(prompt(" Informe o 1° número"))
let n2 = Number(prompt("Informe o 2° número"))
let soma = n1+n2
let sub = n1-n2
let mult = n1*n2
let div = n1/n2
document.write(`A soma dos números é ${soma}`)
document.write(`<br>`)
document.write(`A subtração dos números é ${sub}`)
document.write(`<br>`)
document.write(`A multiplicação dos números é ${mult}`)
document.write(`<br>`)
document.write(`A divisão dos números é ${div}`)